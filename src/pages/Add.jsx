import axios from "axios";
import React from "react";
import { useState } from "react";
import { useNavigate } from "react-router-dom";

const Add = () => {
  const [book, setBook] = useState({
    title: "",
    desc: "",
    price: null,
    cover: "",
  });

  const navigate = useNavigate();

  const handleChange = (e) => {
    setBook((prev) => ({ ...prev, [e.target.name]: e.target.value }));
  };

  const handleClick = async (e) => {
    e.preventDefault();
    try {
      await axios.post("http://localhost:8800/books", book);
      navigate("/");
    } catch (err) {
      console.log(err);
    }
  }; // preventDefault stops the reload of page when clicking the button navigate takes us to home page if the adding is successul...post method to add something new to backend

  console.log(book);
  return (
    <div className="form">
      <h1>Add New Book</h1>{" "}
      {/* make sure the names match the state names then create the handleChange function sothings update */}
      <input
        type="text"
        placeholder="title"
        onChange={handleChange}
        name="title"
      />
      <input
        type="text"
        placeholder="desc"
        onChange={handleChange}
        name="desc"
      />
      <input
        type="number"
        placeholder="price"
        onChange={handleChange}
        name="price"
      />
      <input
        type="text"
        placeholder="cover"
        onChange={handleChange}
        name="cover"
      />
      <button className="formBtn" onClick={handleClick}>
        Add
      </button>
    </div>
  );
};
//then make a handleClick function to send the new data to our backend
export default Add;
